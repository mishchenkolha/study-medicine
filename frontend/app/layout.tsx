import type { Metadata } from 'next';
import { Geist, Geist_Mono } from 'next/font/google';
import '@/styles/globals.css';
import Navbar from '@/components/navbar';
import { getCategoriesTree, getNavbar } from '@/services/navbar.service';
import { normalizeMenu } from '@/utils/menu';
import Footer from '@/components/footer';
import { getDictionary } from '@/services/dictionary.service';
import { ToastContainer } from 'react-toastify';
import Script from 'next/script';

export const dynamic = 'force-dynamic';

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
});

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const dictionaryPromise = getDictionary();
  const navBarPromise = getNavbar('main_menu');
  const footerMenuPromise = getNavbar('footer');
  const categoriesPromise = getCategoriesTree();
  const dictionary = await dictionaryPromise;
  const navBar = await navBarPromise;
  const footerMenu = await footerMenuPromise;
  const categoriesTree = await categoriesPromise;
  const menu = normalizeMenu(navBar, categoriesTree);

  return (
    <html lang="en">
      <head>
        {/* Next.js сам перенесе цей скрипт у head завдяки стратегії beforeInteractive */}
        <Script
          id="fb-pixel"
          strategy="beforeInteractive"
          dangerouslySetInnerHTML={{
            __html: `
              !function(f,b,e,v,n,t,s)
              {if(f.fbq)return;n=f.fbq=function(){n.callMethod?
              n.callMethod.apply(n,arguments):n.queue.push(arguments)};
              if(!f._fbq)f._fbq=n;n.push=n;n.loaded=!0;n.version='2.0';
              n.queue=[];t=b.createElement(e);t.async=!0;
              t.src=v;s=b.getElementsByTagName(e)[0];
              s.parentNode.insertBefore(t,s)}(window, document,'script',
              'https://connect.facebook.net/en_US/fbevents.js');
              fbq('init', '1319595660118995');
              fbq('track', 'PageView');
            `,
          }}
        />
      </head>
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        <Navbar menu={menu} dictionary={dictionary} />
        <main className="container flex min-h-[calc(100vh-68px)] flex-col pt-32">
          {children}
        </main>
        <Footer dictionary={dictionary} menu={footerMenu} />
        <ToastContainer />
      </body>
    </html>
  );
}
